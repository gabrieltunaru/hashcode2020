public class Library {
    public String id;
    public int maxBooks;
    public int signUpDuration;
    public int maxBooksDelivered;

    public Library(String id, int maxBooks, int signUpDuration, int maxBooksDelivered) {
        this.id = id;
        this.maxBooks = maxBooks;
        this.signUpDuration = signUpDuration;
        this.maxBooksDelivered = maxBooksDelivered;
    }


}
